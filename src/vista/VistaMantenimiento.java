    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int atraccionId = Integer.parseInt(txtAtraccionId.getText());
        String estadoAtraccion = (String) txtEstado.getSelectedItem();
        
        AtraccionControlador atraccionControlador = new AtraccionControlador();
        Atraccion atraccion = atraccionControlador.obtenerAtraccionPorId(atraccionId);
        
        Atraccion nuevaAtraccion = new Atraccion(atraccion.getId(),atraccion.getNombre(), atraccion.getDescripcion(), atraccion.getClasificacion(), atraccion.getCondicionesUso(),estadoAtraccion,atraccion.getAlturaMinima());
        atraccionControlador.actualizarAtraccion(atraccionId, nuevaAtraccion);
        
        JOptionPane.showMessageDialog(this, "Atracción actualizada correctamente.");
        
    }//GEN-LAST:event_btnActualizarActionPerformed

