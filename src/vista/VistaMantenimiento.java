    public void cargarAtraccionesEnTabla(javax.swing.JTable tablaAtracciones) {
        // Obtener el modelo asociado a la tabla proporcionada
        DefaultTableModel modelo = (DefaultTableModel) tablaAtracciones.getModel();

        // Limpiar todas las filas existentes en la tabla
        modelo.setRowCount(0);

        // Instanciar el controlador de atracciones para acceder a los datos
        AtraccionControlador atraccionControlador = new AtraccionControlador();
        List<Atraccion> atracciones = atraccionControlador.obtenerTodasLasAtracciones();

        // Iterar sobre la lista de atracciones y agregar cada una como una fila a la tabla
        for (Atraccion atraccion : atracciones) { 
            modelo.addRow(new Object[]{
                atraccion.getId(),      // ID único de la atracción
                atraccion.getNombre(),  // Nombre descriptivo de la atracción
                atraccion.getEstado()   // Estado actual de la atracción (e.g., activa/inactiva)
            });
        }
    }
    
